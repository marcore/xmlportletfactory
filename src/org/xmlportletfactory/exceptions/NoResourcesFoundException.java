import java.util.logging.Level;
import java.util.logging.Logger;

/**
 *
 * @author Jack A. Rider
 */
public class NoResourcesFoundException extends Exception {

    public NoResourcesFoundException(String resourceDir) {
        Logger.getLogger(NoResourcesFoundException.class.getName()).log(Level.SEVERE, "No Resources found in : "+resourceDir);
    }

}
